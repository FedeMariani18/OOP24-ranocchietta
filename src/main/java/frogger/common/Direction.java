package frogger.common;

/**
 * Represents the possible directions in which an object (such as the player) can face or move.
 */
public enum Direction {
    /**
     * The upward direction.
     */
    UP,
    /**
     * The downward direction.
     */
    DOWN,
    /**
     * The rightward direction.
     */
    RIGHT,
    /**
     * The leftward direction.
     */
    LEFT
}
